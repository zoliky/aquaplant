  <h1 class="title">Intretinere nuferi</h1>
  <p>Plantele acvatice sunt un necesar pentru o apa sanatoasa si curata in iaz. Folosindu-le veti
  putea controla problema inmultirii algelor si a aspectului verzui al apei. Dintre acestea, nuferii
  sunt cele mai indragite plante de apa, care aduc gratie si o senzatie de liniste oricarei gradini.
  Ei cer foarte putin in schimbul frumusetii pe care v-o ofera: caldura soarelui, ape linistite si
  o fertilizare regulata sunt singurele cerinte.</p>
  <p>Pe langa aspectul estetic nuferii  contribuie la crearea unui climat optim in iaz. Ei oxigeneaza
  apa pe timpul zilei si reduc nitratii responsabili de formarea algelor. Frunzele asigura umbra 
  necesara iazului ferind pestii de caldura, oferindu-le un loc unde sa-si depuna ouale si protejeaza
  puii de pesti de riscul de a fi mancati de adulti.</p>
  <h2 class="title">Nume botanic</h2>
  <p class="descr">Nymphaea.</p>
  <h2 class="title">Categoria</h2>
  <p class="descr">Plante de iaz perene, durata lunga de viata.</p>
  <h2 class="title">Perioada inflorire</h2>
  <p class="descr">Primavara devreme, pana toamna tarziu. Florile se deschid in perioada cea mai
  calda a zilei si dureaza 4 zile fiecare. O planta poate produce 6 sau mai multe flori/zi.</p>
  <h2 class="title">Pozitionare</h2>
  <p class="descr">Prefera locuri insorite cu minim 4-6 ore de soare plin, pana la usor umbrit. 
  Soarele de dupa amiaza este mai bun decat cel de dimineata fiind mai intens. Mai mult soare
  inseamna mai multe flori. Daca iazul este pozitionat intr-un loc umbros, cu mai putin de 4 ore 
  de soare/zi, planta va da frunze dar nu va inflori. Nuferii se dezvolta armonios in ape
  statatoare, nu aproape de cascade si fantani arteziene.</p>
  <h2 class="title">Adancime</h2>
  <p class="descr">Minim 20-40 cm pana la 1,20 m, optim 40-60 cm. Cu cat ghiveciul este mai aproape
  de suprafata, cu atat apa este mai calda si plantele vor creste si inflori mai repede. Primavara, 
  cand temperature apei depaseste 10 C planta se aseaza la o adancime mica (cativa cm peste nivelul
  solului), iar pe masura ce cresc frunzele, tot mai adanc pana la adancimea optima fiecarei
  varietati.</p>
  <h2 class="title">Transplantare</h2>
  <p class="descr">Incepand din primavara pana vara tarziu.</p>
  <h2 class="title">Divizare</h2>
  <p class="descr">Se pot obtine cu succes mai multe plante viguroase, prin divizarea plantei mama
  o data la 3-4 ani, in martie-aprilie.</p>
  <h2 class="title">Intretinere</h2>
  <p class="descr">Frunzele si florile ofilite se inlatura prin taiere pentru a avita problema
  decompunerii lor si pentru a mentine un bun echilibru al apei.</p>
  <h2 class="title">Sol</h2>
  <p class="descr">Pamant bogat in substante organice, acoperit cu un strat de pietris, pentru a nu
  se  imprastia in iaz.</p>
  <h2 class="title">Iernare</h2>
  <p class="descr">Fara probleme in ape cu adancime de minim 35-40 cm, pentru a preveni inghetarea
  radacinilor. O alternativa este depozitarea ghivecelor intr-o incapere cu temperatura de cca 10 C.
  Pamantul se mentine umed. Inainte de primul inghet frunzele se inlatura (urmand sa apara altele 
  noi in primavara urmatoare),iar ghivecele se muta in locul ales pentru iernare.</p>
  <h2 class="title">Rezistenta la boli</h2>
  <p class="descr">Foarte buna, rareori sunt afectati.</p>
  <h2 class="title">Ghivece</h2>
  <p class="descr">Optim diam. 20cm sau mai mari, putin adanci, cu spatiu mai mult pe orizontala.
  Marimea pe care o va atinge planta depinde de marimea ghiveciului in care este plantata.</p>
  <h2 class="title">Fertilizare</h2>
  <p class="descr">Pentru o inflorire abundenta si de lunga durata utilizati tablete fertilizante
  la transplantare si pe perioada verii, prin simpla introducere a tabletelor in pamant.</p>
  <h2 class="title">Comentarii</h2>
  <p class="descr">Pestii de iaz vor fi fericiti sa inoate printre nuferi.</p>
